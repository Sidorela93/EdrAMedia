    function sendToMailchimp(){
        var err = false;
        $('.required').each(function( index ) {
            if($(this).val() == ''){
                err = true;
            }
        });

        if(!$('#privacy1').is(':checked') && !$('#privacy2').is(':checked') && !$('#privacy3').is(':checked')){
            err = true;
        }

        if(!err){
            $.ajax({
                type: 'POST',
                url: "ajax/send_subscription.ajax.php",
                data: $('#form-newsletter').serializeArray(),
                async:false
                }).done(function(data) {
                json = jQuery.parseJSON(data.trim());
                if(json.errore == 0){
                    Swal.fire(
                        'CONGRATULATIONS!',
                        json.msg,
                        'success'
                    )
                }
                else{
                    Swal.fire(
                        'ATTENTION!',
                        json.msg,
                        'error'
                    )
                }
            });
        }
        else{
            Swal.fire(
                'ATTENTION!',
                'There something wrong with your request. Check if all of required fields are filled.',
                'error'
            )
        }
        
    }

